    private void preencherTabela() {
        try {
            conectaDAO dao = new conectaDAO();
            boolean situacao;
            situacao = dao.connectDB();

            if (!situacao) {
                JOptionPane.showMessageDialog(null, "Erro de conex√£o");
            } else {
                ProdutosDAO produtosdao = new ProdutosDAO(dao.conn);
                DefaultTableModel model = (DefaultTableModel) tbVendidos.getModel();
                model.setNumRows(0);

                ArrayList<ProdutosDTO> listagem = produtosdao.listarProdutosVendidos();

                for (ProdutosDTO produto : listagem) {
                    model.addRow(new Object[]{
                        produto.getId(),
                        produto.getNome(),
                        produto.getValor(),
                        produto.getStatus()
                    });
                }
                dao.desconectar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao listar produtos: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

